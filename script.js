input.addEventListener("keyup", searchHandler);

function searchHandler(e) {
  let userData = e.target.value;
  let results = search(userData);
  showSuggestions(results, userData);
}
function useSuggestion(e) {
  let useSuggestionData = e.textContent;
  input.value = useSuggestionData;
  suggestions.innerHTML = "";
}
function showSuggestions(results, inputVal) {
  suggestions.innerHTML = results.map((list) => "<li>" + list + "</li>").join(" ");
  if (inputVal === "") {
    suggestions.innerHTML = "";
  }
  let allList = suggestions.querySelectorAll("li");
  for (let i = 0; i < allList.length; i++) {
    allList[i].setAttribute("onclick", "useSuggestion(this)");
  }
}

function search(str) {
  return fruit.filter((data) => data.toLocaleLowerCase().includes(str.toLocaleLowerCase()));
}

